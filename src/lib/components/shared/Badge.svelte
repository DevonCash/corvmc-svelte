<script lang="ts">
	import type { Snippet } from 'svelte';

	let {
		variant,
		size = 'sm',
		class: className = '',
		children
	}: {
		variant?: 'outline' | 'ghost' | 'primary' | 'success' | 'error' | 'warning' | 'info' | 'secondary';
		size?: 'xs' | 'sm' | 'md' | 'lg';
		class?: string;
		children: Snippet;
	} = $props();
</script>

<span class="badge {variant ? `badge-${variant}` : ''} {size !== 'md' ? `badge-${size}` : ''} {className}">
	{@render children()}
</span>
